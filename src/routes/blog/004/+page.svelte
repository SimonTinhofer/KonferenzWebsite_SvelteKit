    <h1>Ohne KI zum KI-Job: Anthropic fordert authentische Bewerbungen</h1>

    <p>
      Anthropic, das Unternehmen hinter dem KI-Assistenten „Claude“, hat Bewerber:innen aufgefordert, bei der Erstellung ihrer Bewerbungsunterlagen keine KI-Assistenten zu verwenden.
      In den Stellenausschreibungen heißt es:
    </p>

    <blockquote>
      „Bitte verwenden Sie während des Bewerbungsprozesses keine KI-Assistenten. Wir möchten Ihr persönliches Interesse an Anthropic ohne Vermittlung durch ein KI-System verstehen und auch Ihre nicht von KI unterstützten Kommunikationsfähigkeiten bewerten.“
    </blockquote>

    <p>
      Abgesehen von einer wohl unbeabsichtigten Ironie zeigt diese Entscheidung, dass die Verwendung von KI-Tools keine Einbahnstraße ist. Während einerseits immer mehr Unternehmen beim Recruiting auf KI-Tools setzen (z.&nbsp;B. zum Auslesen von Lebensläufen und/oder zur Reihung von Bewerbungen), gibt es andererseits auch immer mehr Bewerber:innen, die sich das Motivationsschreiben von einem KI-Chatbot verfassen lassen.
    </p>

    <p>
      Auf YouTube gibt es zahllose Erklärvideos, wie man die „digitalen Torwächter“ des Wunschunternehmens überlisten kann. Aber auch Arbeitnehmer:innen nutzen immer häufiger ChatGPT&nbsp;&amp;&nbsp;Co, vielfach ohne das Wissen bzw. die Genehmigung ihrer Arbeitgeber:innen.
    </p>

